public class SwitchExercises {

    public static void main(String[] args) {
        int dayOfWeek = 4;

        switch (dayOfWeek) {
            case 1:
                System.out.println("The animal is a lion");
            case 2:
                System.out.println("Animal is Dog");
            case 4:
                System.out.println("No animal selected");
                break;
            default:
                System.out.println("No animal present");
        }
    }


}
